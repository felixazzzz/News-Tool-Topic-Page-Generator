def extract_domain(url: str) -> str:
    """Return bare domain, stripping www."""
    return urlparse(url).netloc.removeprefix("www.")


def get_domain_tier(domain: str) -> str:
    """Return credibility tier. Subdomain overrides take precedence over the main tier table."""
    if domain in _SUBDOMAIN_OVERRIDES:
        return _SUBDOMAIN_OVERRIDES[domain]
    return _DOMAIN_TIER.get(domain, "tier_3_media")


def is_platform(domain: str) -> bool:
    return domain in _PLATFORM_DOMAINS


def is_blocklisted(domain: str) -> bool:
    return domain in _BLOCKLIST_DOMAINS


def is_media_outlet(domain: str) -> bool:
    """Return True if domain is a known media outlet (listed in any credibility tier)."""
    return domain in _DOMAIN_TIER


# Matches non-English locale path segments: /zh-hans-cn/, /ru-ru/, /pl-pl/, etc.
_I18N_RE = re.compile(r"/([a-z]{2}-[a-z]{2,}(?:-[a-z]{2,})?)/")


def is_non_english_i18n(url: str) -> bool:
    """Return True if URL is a non-English i18n variant (e.g. /zh-hans-cn/, /ru-ru/)."""
    path = urlparse(url).path.lower()
    m = _I18N_RE.search(path)
    if not m:
        return False
    lang = m.group(1).split("-")[0]
    return lang != "en"


