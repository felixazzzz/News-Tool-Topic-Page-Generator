def _tier1_credibility(domain: str) -> str:
    if domain in _SUBDOMAIN_OVERRIDES:
        return _SUBDOMAIN_OVERRIDES[domain]
    return "primary_disputed" if is_blocklisted(domain) else "primary"


# Standard primary document patterns per event type.
# These supplement Stage A's queries to ensure canonical primary documents
# (system cards, schedules, brackets, official programs) are always targeted.
_PRIMARY_DOC_PATTERNS: dict[str, list[str]] = {
    "product_launch":  ["system card", "release notes"],
    "sports_event":    ["official schedule", "bracket", "squad list"],
    "scheduled_event": ["official program", "schedule", "lineup"],
    "live_event":      ["official program", "running order"],
}


def _tier1_queries(plan: Plan) -> list[tuple[str, str]]:
    """
    Build (query, domain) pairs for Tier 1 discovery.

    Priority order:
    1. site: queries already generated by Stage A (most specific)
    2. Fallback: site:<domain> <normalized_title> for any candidate not yet covered
    3. Supplementary: per-event-type document pattern queries (system card, schedule, etc.)
    """
    queries: list[tuple[str, str]] = []
    seen_queries: set[str] = set()

    def _add(q: str, domain: str) -> None:
        if q not in seen_queries:
            queries.append((q, domain))
            seen_queries.add(q)

    # 1. Stage A's own site: queries
    for q in plan.search_queries:
        m = re.search(r"site:(\S+)", q, re.IGNORECASE)
        if m:
            domain = m.group(1).lower().rstrip("/")
            if not is_platform(domain) and not is_media_outlet(domain):
                _add(q, domain)

    # 2 + 3. For each primary candidate: fallback title query + doc-pattern queries
    doc_patterns = _PRIMARY_DOC_PATTERNS.get(plan.event_type, [])
    for raw in plan.primary_source_candidates:
        domain = raw.lower().rstrip("/")
        if is_platform(domain):
            logger.debug("Tier 1 skip (platform): %s", domain)
            continue
        if is_media_outlet(domain):
            logger.info("Tier 1 skip (media outlet, rerouted to Tier 2): %s", domain)
            continue
        # Fallback title query
        _add(f"site:{domain} {plan.normalized_title}", domain)
        # Document-type supplementary queries
        for pattern in doc_patterns:
            _add(f"site:{domain} {pattern}", domain)

    return queries


def discover_tier1_urls(plan: Plan, tavily: TavilyClient) -> list[dict]:
    """
    For each primary source candidate, run a site:-scoped Tavily search to
    find candidate URLs. Returns raw URL metadata dicts; body fetch is subtask 5.
    """
    cache = get_cache()
    results: list[dict] = []

    for query, domain in _tier1_queries(plan):
        cache_key = f"search:{search_cache_key(query, max_results=5, tier=1)}"
        cached = cache.get(cache_key)

        if cached is not None:
            logger.debug("Tier 1 cache hit: %s", query[:60])
            raw = cached
        else:
            try:
                resp = tavily.search(
                    query=query,
                    search_depth="basic",
                    max_results=5,
                )
                raw = resp.get("results", [])
                cache.set(cache_key, raw)
                logger.debug("Tier 1 Tavily search: %s → %d results", query[:60], len(raw))
            except Exception as exc:
                logger.warning("Tier 1 search failed (%s): %s", domain, exc)
                continue

        for r in raw:
            url = r.get("url", "")
            result_domain = extract_domain(url)
            # Keep only URLs that belong to the target domain
            if result_domain != domain and not result_domain.endswith(f".{domain}"):
                continue
            # Drop non-English i18n variants
            if is_non_english_i18n(url):
                logger.debug("Tier 1 skip (non-English i18n): %s", url)
                continue
            results.append({
                "url": url,
                "title": r.get("title", ""),
                "domain": domain,
                "credibility_tier": _tier1_credibility(domain),
                "retrieval_tier": 1,
                "published_at": r.get("published_date"),
            })

    logger.info("Tier 1 discovered %d candidate URL(s)", len(results))
    return results


