_FETCH_HEADERS = {
    "User-Agent": (
        "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
        "AppleWebKit/537.36 (KHTML, like Gecko) "
        "Chrome/124.0.0.0 Safari/537.36"
    ),
    "Accept-Language": "en-US,en;q=0.9",
}
_ENTITY_MATCH_THRESHOLD = 80  # rapidfuzz partial_ratio minimum for a hit
_MIN_CONTENT_CHARS = 200       # trafilatura extractions below this are noise

# ── Title noise detection ────────────────────────────────────────────────────

_TITLE_NOISE_FRAGMENTS = (
    "need cookies", "cookie", "hauptnavigation", "navigation", "menu",
    "accept all", "privacy settings", "newsletter", "subscribe", "sign up",
    "log in", "login", "sign in",
)

def _is_noisy_title(title: str) -> bool:
    t = title.strip()
    if not t or len(t) < 5:
        return True
    t_lower = t.lower()
    if any(frag in t_lower for frag in _TITLE_NOISE_FRAGMENTS):
        return True
    # Single bare word (likely a nav label)
    if len(t.split()) == 1 and not any(c.isdigit() for c in t):
        return True
    return False


def _fallback_title(html: str, url: str) -> str:
    """Try og:title → twitter:title → first <h1> → URL slug."""
    # og:title (two attribute orderings)
    for pat in (
        r'<meta[^>]+property=["\']og:title["\'][^>]+content=["\']([^"\']{5,})["\']',
        r'<meta[^>]+content=["\']([^"\']{5,})["\'][^>]+property=["\']og:title["\']',
    ):
        m = re.search(pat, html, re.IGNORECASE)
        if m:
            return m.group(1).strip()
    # twitter:title
    for pat in (
        r'<meta[^>]+name=["\']twitter:title["\'][^>]+content=["\']([^"\']{5,})["\']',
        r'<meta[^>]+content=["\']([^"\']{5,})["\'][^>]+name=["\']twitter:title["\']',
    ):
        m = re.search(pat, html, re.IGNORECASE)
        if m:
            return m.group(1).strip()
    # First <h1>
    m = re.search(r"<h1[^>]*>([^<]{5,})</h1>", html, re.IGNORECASE)
    if m:
        return re.sub(r"\s+", " ", m.group(1)).strip()
    # URL slug
    path = urlparse(url).path.rstrip("/").split("/")[-1]
    slug = re.sub(r"[-_]", " ", re.sub(r"\.\w+$", "", path)).strip().title()
    return slug or url

# ── published_at sanity check ────────────────────────────────────────────────

_DATE_FLOOR = datetime(2010, 1, 1, tzinfo=timezone.utc)

def _sanitize_date(date_str: str | None) -> str | None:
    if not date_str:
        return None
    try:
        dt = _parse_partial_date(date_str)
        if dt < _DATE_FLOOR or dt > datetime.now(tz=timezone.utc) + timedelta(days=365):
            return None
        return date_str
    except Exception:
        return None


def _entity_match(body: str, key_entities: list) -> bool:
    """Return True if any key_entity name fuzzy-matches the body (partial_ratio ≥ threshold)."""
    body_lower = body.lower()
    for entity in key_entities:
        if fuzz.partial_ratio(entity.name.lower(), body_lower) >= _ENTITY_MATCH_THRESHOLD:
            return True
    return False


async def _fetch_one(client: httpx.AsyncClient, url: str, cache) -> dict | None:
    """Fetch a single URL, extract text + metadata via trafilatura. Returns None on any failure."""
    cache_key = f"fetch:{fetch_cache_key(url)}"
    cached = cache.get(cache_key)
    if cached is not None:
        logger.debug("Fetch cache hit: %s", url[:80])
        return cached

    try:
        resp = await client.get(url, follow_redirects=True, timeout=15.0)
        resp.raise_for_status()
        html = resp.text
    except Exception as exc:
        logger.warning("Fetch failed %s: %s", url[:80], exc)
        return None

    text = trafilatura.extract(html, include_comments=False, include_tables=True)
    if not text:
        logger.warning("trafilatura returned empty: %s", url[:80])
        return None

    meta = trafilatura.extract_metadata(html, default_url=url)
    raw_title = (meta.title or "").strip() if meta else ""
    if _is_noisy_title(raw_title):
        raw_title = _fallback_title(html, url)
    result = {
        "content": text,
        "published_at_page": meta.date if meta else None,
        "meta_title": raw_title,
        "fetched_at": datetime.now(tz=timezone.utc).isoformat(),
    }
    cache.set(cache_key, result)
    return result


async def _fetch_all(candidates: list[dict]) -> list[tuple[dict, dict | None]]:
    cache = get_cache()
    async with httpx.AsyncClient(headers=_FETCH_HEADERS) as client:
        tasks = [_fetch_one(client, r["url"], cache) for r in candidates]
        results = await asyncio.gather(*tasks)
    return list(zip(candidates, results))


def fetch_and_filter_bodies(
    candidates: list[dict],
    plan: Plan,
) -> tuple[list[dict], dict]:
    """
    Fetch body content for all candidates, then apply key-entity fuzzy filter.
    Returns (enriched_list, stats_dict).
    """
    pairs = asyncio.run(_fetch_all(candidates))

    stats: dict[str, int] = defaultdict(int)
    stats["input"] = len(candidates)
    entity_kills: list[str] = []
    enriched: list[dict] = []

    for candidate, fetch_result in pairs:
        url = candidate["url"]

        if fetch_result is None:
            stats["removed_fetch_failed"] += 1
            continue

        content = fetch_result["content"]

        if len(content) < _MIN_CONTENT_CHARS:
            stats["removed_too_short"] += 1
            logger.info("Skip (content %d chars): %s", len(content), url[:80])
            continue

        # Tier 1 sources were targeted via site:-scoped queries for this exact event;
        # entity filter would produce false negatives on technical primary docs.
        if candidate.get("retrieval_tier") != 1:
            if not _entity_match(content, plan.key_entities):
                stats["removed_entity_mismatch"] += 1
                entity_kills.append(url)
                logger.info("Skip (entity mismatch): %s", url[:80])
                continue

        # Prefer trafilatura's date over Tavily's; fall back to whatever Tavily gave us
        pub = _sanitize_date(
            fetch_result.get("published_at_page") or candidate.get("published_at")
        )
        title = fetch_result.get("meta_title") or candidate.get("title", "")

        enriched.append({
            **candidate,
            "content": content,
            "published_at": pub,
            "title": title,
            "fetched_at": fetch_result["fetched_at"],
        })

    stats["output"] = len(enriched)
    stats["_entity_kills"] = entity_kills  # for reporting; stripped before return
    return enriched, dict(stats)


# ---------------------------------------------------------------------------
# Subtask 6 — Near-duplicate detection (MinHash / Jaccard)
# ---------------------------------------------------------------------------

_MINHASH_NUM_PERM = 128          # permutations; balances accuracy vs. speed
_NEAR_DUP_THRESHOLD = 0.85       # Jaccard ≥ 0.85 → same content


def _minhash(text: str) -> MinHash:
    """Build a MinHash from 3-gram shingles of the text."""
    mh = MinHash(num_perm=_MINHASH_NUM_PERM)
    words = text.lower().split()
    for i in range(max(1, len(words) - 2)):
        shingle = " ".join(words[i : i + 3])
        mh.update(shingle.encode("utf-8"))
    return mh


def deduplicate_near_duplicates(sources: list[dict]) -> tuple[list[dict], dict]:
    """
    Cluster sources by MinHash Jaccard similarity and keep one representative
    per cluster: prefer higher credibility tier, then more recent published_at.

    Returns (deduped_list, stats_dict).
    """
    if not sources:
        return [], {"input": 0, "output": 0, "removed_near_dup": 0}

    minhashes = [_minhash(s["content"]) for s in sources]

    # Union-Find for clustering
    parent = list(range(len(sources)))

    def find(x: int) -> int:
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(a: int, b: int) -> None:
        parent[find(a)] = find(b)

    for i in range(len(sources)):
        for j in range(i + 1, len(sources)):
            if minhashes[i].jaccard(minhashes[j]) >= _NEAR_DUP_THRESHOLD:
                union(i, j)
                logger.debug(
                    "Near-dup pair (j=%.2f): %s  ~  %s",
                    minhashes[i].jaccard(minhashes[j]),
                    sources[i]["url"][:60],
                    sources[j]["url"][:60],
                )

    # Group by cluster root
    clusters: dict[int, list[int]] = defaultdict(list)
    for idx in range(len(sources)):
        clusters[find(idx)].append(idx)

    # Pick representative: best credibility tier, then most recent date
    kept: list[dict] = []
    for members in clusters.values():
        best = min(
            members,
            key=lambda i: (
                _TIER_RANK.get(sources[i].get("credibility_tier", "tier_3_media"), 5),
                _date_sort_key(sources[i].get("published_at")),
            ),
        )
        kept.append(sources[best])

    removed = len(sources) - len(kept)
    stats = {"input": len(sources), "removed_near_dup": removed, "output": len(kept)}
    if removed:
        logger.info("Near-dup dedup: %d → %d (removed %d)", len(sources), len(kept), removed)
    return kept, stats


