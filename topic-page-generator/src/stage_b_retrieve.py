# URL path patterns that indicate non-article pages (tag pages, category indexes, etc.)
_URL_PATH_BLOCKLIST = re.compile(
    r"(/tag/|/tags/|/category/|/categories/"
    r"|/author/|/authors/|/feed/|/rss/"
    r"|/topic/|/topics/|/page/\d+|/search\b)",
    re.IGNORECASE,
)

# URL query-string patterns indicating pagination or aggregation
_URL_QUERY_BLOCKLIST = re.compile(r"[?&](page|p)=\d+", re.IGNORECASE)

# Per-tier source caps applied after deduplication (primary noise filtered by subtask 5 key-entity check)
_PER_TIER_CAPS: dict[str, int] = {
    "primary":          12,
    "primary_disputed":  5,
    "tier_1_media":     10,
    "tier_2_media":     10,
    "tier_3_media":     10,
    "low_quality":       3,
}
_TOTAL_CAP = 25


def _matches_url_blocklist(url: str) -> bool:
    path = urlparse(url).path
    query = urlparse(url).query
    return bool(_URL_PATH_BLOCKLIST.search(path) or _URL_QUERY_BLOCKLIST.search(query))


def _is_gpt_store_url(url: str) -> bool:
    """chatgpt.com/g/<id> paths are third-party GPTs, not official OpenAI content."""
    parsed = urlparse(url)
    return parsed.netloc.removeprefix("www.") == "chatgpt.com" and parsed.path.startswith("/g/")


def _has_non_english_locale_param(url: str) -> bool:
    """Filter URLs with ?locale=xx-XX where language is not English."""
    params = parse_qs(urlparse(url).query)
    locale = params.get("locale", [""])[0].lower()
    if not locale:
        return False
    return locale.split("-")[0] not in ("en", "")


def _parse_partial_date(date_str: str) -> datetime:
    """Parse ISO 8601 partial date (YYYY, YYYY-MM, YYYY-MM-DD) to datetime (UTC)."""
    parts = date_str.split("-")
    try:
        if len(parts) == 1:
            return datetime(int(parts[0]), 1, 1, tzinfo=timezone.utc)
        if len(parts) == 2:
            return datetime(int(parts[0]), int(parts[1]), 1, tzinfo=timezone.utc)
        return datetime.strptime(date_str[:10], "%Y-%m-%d").replace(tzinfo=timezone.utc)
    except ValueError:
        return datetime(2000, 1, 1, tzinfo=timezone.utc)


def _in_time_window(published_at: str, key_date: str, window_days: int) -> bool:
    """Return True if published_at falls within [key_date - window_days, now + 7 days]."""
    try:
        pub = _parse_partial_date(published_at)
        anchor = _parse_partial_date(key_date)
        now = datetime.now(tz=timezone.utc)
        start = anchor - timedelta(days=window_days)
        end = now + timedelta(days=7)
        return start <= pub <= end
    except Exception:
        return True  # if we can't parse, don't filter


def _normalize_url(url: str) -> str:
    """Normalize URL for deduplication: lowercase host, strip trailing slash, remove tracking params."""
    parsed = urlparse(url)
    tracking = {"utm_source", "utm_medium", "utm_campaign", "utm_content", "utm_term", "ref", "source"}
    params = {k: v for k, v in parse_qs(parsed.query).items() if k.lower() not in tracking}
    clean_query = urlencode(params, doseq=True)
    path = parsed.path.rstrip("/") or "/"
    host = parsed.netloc.lower().removeprefix("www.")
    return f"{parsed.scheme}://{host}{path}{'?' + clean_query if clean_query else ''}"


def filter_urls(
    candidates: list[dict],
    plan: "Plan",
    time_window_days: int = 90,
) -> tuple[list[dict], dict]:
    """
    Apply all deterministic filters to URL candidates.
    Returns (kept_list, stats_dict) where stats shows per-rule removal counts.
    URLs without published_at are NOT filtered by time window (kept, fate decided at fetch time).
    """
    stats: dict[str, int] = defaultdict(int)
    stats["input"] = len(candidates)
    seen_norm: set[str] = set()
    kept: list[dict] = []

    for r in candidates:
        url = r.get("url", "")
        domain = r.get("domain") or extract_domain(url)

        if is_blocklisted(domain):
            stats["removed_blocklist"] += 1
            logger.debug("FILTER blocklist      %s", url)
            continue

        if _is_gpt_store_url(url):
            stats["removed_gpt_store"] += 1
            logger.debug("FILTER gpt_store      %s", url)
            continue

        if _matches_url_blocklist(url):
            stats["removed_url_pattern"] += 1
            logger.debug("FILTER url_pattern    %s", url)
            continue

        if is_non_english_i18n(url):
            stats["removed_i18n_path"] += 1
            logger.debug("FILTER i18n_path      %s", url)
            continue

        if _has_non_english_locale_param(url):
            stats["removed_i18n_locale_param"] += 1
            logger.debug("FILTER i18n_param     %s", url)
            continue

        pub = r.get("published_at")
        if pub and not _in_time_window(pub, plan.key_date, time_window_days):
            stats["removed_time_window"] += 1
            logger.debug("FILTER time_window    %s (pub: %s)", url, pub)
            continue

        norm = _normalize_url(url)
        if norm in seen_norm:
            stats["removed_duplicate"] += 1
            logger.debug("FILTER duplicate      %s", url)
            continue
        seen_norm.add(norm)

        kept.append(r)

    # Sort by tier rank + recency before capping so higher-quality sources survive
    kept.sort(key=lambda r: (
        _TIER_RANK.get(r.get("credibility_tier", "tier_3_media"), 5),
        _date_sort_key(r.get("published_at")),
    ))

    # Per-tier caps + total safety cap
    tier_counts: dict[str, int] = defaultdict(int)
    capped: list[dict] = []
    for r in kept:
        tier = r.get("credibility_tier", "tier_3_media")
        if tier_counts[tier] >= _PER_TIER_CAPS.get(tier, 5):
            stats["removed_tier_cap"] += 1
            logger.debug("FILTER tier_cap       %s (tier: %s)", r["url"], tier)
            continue
        if len(capped) >= _TOTAL_CAP:
            stats["removed_total_cap"] += 1
            logger.debug("FILTER total_cap      %s", r["url"])
            continue
        tier_counts[tier] += 1
        capped.append(r)

    stats["output"] = len(capped)
    return capped, dict(stats)


