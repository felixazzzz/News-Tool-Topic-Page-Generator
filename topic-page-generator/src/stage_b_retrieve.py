def _make_slug(text: str) -> str:
    clean = re.sub(r"[^a-z0-9\s]", "", text.lower())
    return "-".join(clean.split()[:6])


def _save(output: SourcesOutput, input_sentence: str) -> None:
    slug = _make_slug(input_sentence)
    out_dir = OUTPUTS_DIR / slug
    out_dir.mkdir(parents=True, exist_ok=True)
    path = out_dir / "sources.json"
    path.write_text(
        json.dumps(output.model_dump(), indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    logger.info("Stage B → %s", path)


def run(plan: Plan, input_sentence: str, tavily: TavilyClient | None = None) -> SourcesOutput | None:
    if tavily is None:
        tavily = TavilyClient(api_key=os.environ["TAVILY_API_KEY"])

    tier1 = discover_tier1_urls(plan, tavily)
    tier2 = search_tier2(plan, tavily)
    filtered, filter_stats = filter_urls(tier1 + tier2, plan)
    logger.info("Filter: %s", filter_stats)

    enriched, fetch_stats = fetch_and_filter_bodies(filtered, plan)
    logger.info("Fetch: %s", {k: v for k, v in fetch_stats.items() if not k.startswith("_")})

    if not enriched:
        logger.warning("Stage B: no sources after fetch+entity filter for: %s", plan.normalized_title)
        return None

    enriched, dedup_stats = deduplicate_near_duplicates(enriched)
    logger.info("Near-dup: %s", dedup_stats)

    sources = [
        Source(
            id=f"src_{i:03d}",
            url=r["url"],
            title=r.get("title", ""),
            domain=r.get("domain") or extract_domain(r["url"]),
            fetched_at=r["fetched_at"],
            published_at=r.get("published_at"),
            credibility_tier=r["credibility_tier"],
            retrieval_tier=r["retrieval_tier"],
            content=r["content"],
        )
        for i, r in enumerate(enriched, start=1)
    ]
    output = SourcesOutput(sources=sources)
    _save(output, input_sentence)
    return output


def _date_sort_key(date_str: str | None) -> str:
    """Sort key for published_at: more recent dates sort first (reverse=False via negation trick).
    We prefix with '0' for known dates (inverted via string complement) and '1' for unknown."""
    if not date_str:
        return "1"
    # Negate lexicographic order: replace each digit d with (9-d)
    negated = "".join(str(9 - int(c)) if c.isdigit() else c for c in date_str)
    return "0" + negated


