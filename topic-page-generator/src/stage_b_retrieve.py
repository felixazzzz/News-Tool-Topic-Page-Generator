# Tier ordering for sort (lower = higher priority)
_TIER_RANK: dict[str, int] = {
    "primary":          0,
    "primary_disputed": 1,
    "tier_1_media":     2,
    "tier_2_media":     3,
    "tier_3_media":     4,
    "low_quality":      5,
}


def _tier2_queries(plan: Plan) -> list[str]:
    """
    Return search_queries that are NOT site:-scoped.
    These are the broad queries used for Tier 2 general search.
    """
    return [q for q in plan.search_queries if not re.search(r"site:\S+", q, re.IGNORECASE)]


def search_tier2(plan: Plan, tavily: TavilyClient) -> list[dict]:
    # Build a set of primary domains from the plan for credibility upgrade
    _primary_domains = {d.lower().rstrip("/") for d in plan.primary_source_candidates}
    """
    Run broad Tavily searches, score each result against domain_credibility,
    and return deduplicated URL metadata sorted by tier then recency.

    Blocklisted and platform domains are dropped. Results with unknown domains
    default to tier_3_media.
    """
    cache = get_cache()
    seen_urls: set[str] = set()
    raw_results: list[dict] = []

    for query in _tier2_queries(plan):
        cache_key = f"search:{search_cache_key(query, max_results=10, tier=2)}"
        cached = cache.get(cache_key)

        if cached is not None:
            logger.debug("Tier 2 cache hit: %s", query[:60])
            batch = cached
        else:
            try:
                resp = tavily.search(
                    query=query,
                    search_depth="basic",
                    max_results=10,
                )
                batch = resp.get("results", [])
                cache.set(cache_key, batch)
                logger.debug("Tier 2 search: %s → %d results", query[:60], len(batch))
            except Exception as exc:
                logger.warning("Tier 2 search failed (%s): %s", query[:40], exc)
                continue

        for r in batch:
            url = r.get("url", "")
            if not url or url in seen_urls:
                continue

            domain = extract_domain(url)

            if is_platform(domain):
                logger.debug("Tier 2 skip (platform): %s", url)
                continue
            if is_blocklisted(domain):
                logger.debug("Tier 2 skip (blocklist): %s", url)
                continue
            if is_non_english_i18n(url):
                logger.debug("Tier 2 skip (non-English i18n): %s", url)
                continue

            # subdomain_overrides take precedence over everything
            if domain in _SUBDOMAIN_OVERRIDES:
                tier = _SUBDOMAIN_OVERRIDES[domain]
            elif domain in _primary_domains or any(domain.endswith(f".{p}") for p in _primary_domains):
                tier = "primary"
            else:
                tier = get_domain_tier(domain)  # defaults to tier_3_media if unknown
            seen_urls.add(url)
            raw_results.append({
                "url": url,
                "title": r.get("title", ""),
                "domain": domain,
                "credibility_tier": tier,
                "retrieval_tier": 2,
                "published_at": r.get("published_date"),
                "_tier_rank": _TIER_RANK.get(tier, 5),
            })

    # Sort: tier rank ascending, then recency descending (None dates go last)
    raw_results.sort(key=lambda r: (r["_tier_rank"], _date_sort_key(r["published_at"])))
    for r in raw_results:
        del r["_tier_rank"]

    logger.info(
        "Tier 2 found %d URL(s) across %d quer%s",
        len(raw_results),
        len(_tier2_queries(plan)),
        "y" if len(_tier2_queries(plan)) == 1 else "ies",
    )
    return raw_results


